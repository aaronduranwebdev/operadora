    private void btnAnhadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnhadirActionPerformed
        try {
            int idContrato;
            String telefono = JOptionPane.showInputDialog(this, "Introduce el número de teléfono", "Atención", JOptionPane.INFORMATION_MESSAGE);
            Double precio = Double.valueOf(JOptionPane.showInputDialog(this, "Introduce el precio", "Atención", JOptionPane.INFORMATION_MESSAGE));

            idContrato = Integer.valueOf(cmbContratos.getSelectedItem().toString().split(" - ")[0]);
            String[] resultado;
            try {
                resultado = BDInsertar.insertarLineaMovil(new LineaMovil(idContrato, telefono, precio));
                if (resultado[0].equals("1")) {
                    Log.escribirLog(Log.INFO, "Línea móvil para el contrato : " + resultado[1]);
                }
                this.dispose();
            } catch (SQLException e) {
                Log.escribirLog(Log.ERROR, "No se ha podido insertar el cliente: " + e.getSQLState());
            }
        } catch (Exception e) {
            System.err.println("No se pudo completar la acción");
        }
    }//GEN-LAST:event_btnAnhadirActionPerformed

