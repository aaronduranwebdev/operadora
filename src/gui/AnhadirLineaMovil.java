    private void txtDNIClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNIClienteKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                Cliente cliente = BDLeer.leerCliente(txtDNICliente.getText());
                ArrayList<Contrato> contratos;
                contratos = BDLeer.listaContratos(txtDNICliente.getText());
                cmbContratos.removeAllItems();
                for (Contrato contrato : contratos) {
                    cmbContratos.addItem(contrato.getId() + " - " + contrato.getIban());
                }
            } catch (SQLException e) {
                System.err.println("No se pudo realizar la acción");
            }
        }
    }//GEN-LAST:event_txtDNIClienteKeyPressed

